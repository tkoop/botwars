<h3>The Rules</h3>
<ul>
<li>Players take turns rolling a die.</li>
<li>When it's one players turn, he can roll as many times as he wants, collecting points.</li>
<li>When/if he decides to quit rolling, before he rolls a one, all the points he rolled during that turn get added to his total.</li>
<li>If he rolls a one, he gets no points for that turn and his turn is over.</li>
<li>First person to 100 points wins.</li>
</ul>


